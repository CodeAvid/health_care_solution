class Drug {
  final int id;
  final String name;
  final String description;
  final String image;
  final int price;
  final int gram;
  final String soldBy;

  Drug({
    required this.id,
    required this.name,
    required this.description,
    required this.image,
    required this.price,
    required this.gram,
    required this.soldBy,
  });
}
